// ESM replacement for __dirname
const __dirname = path.dirname(fileURLToPath(import.meta.url))

const IS_JSON = process.argv.includes("--json")

const robotLog = (...args) => {
    if (IS_JSON) {
        console.error(...args)
    } else {
        console.log(...args)
    }
}

// Try to import dotenv if available
let dotenv
try {
    // Use dynamic import for optional dependency
    dotenv = await import("dotenv")
} catch {
    // dotenv might not be installed in all environments
}

// --- GENERIC SETUP & UTILITIES ---
const REPO_ROOT = path.resolve(__dirname, "../../../..")

function readJson(filePath) {
    if (!fs.existsSync(filePath)) {
        console.warn(
            `⚠️  File not found: ${path.relative(
                REPO_ROOT,
                filePath
            )}. Returning empty object.`
        )
        return {}
    }
    try {
        let content = fs.readFileSync(filePath, "utf8")
        if (content.trim() === "") {
            console.warn(
                `⚠️  File is empty: ${path.relative(
                    REPO_ROOT,
                    filePath
                )}. Returning empty object.`
            )
            return {}
        }
        if (content.charCodeAt(0) === 0xfeff) content = content.slice(1)
        return JSON.parse(content)
    } catch {
        console.error(
            `❌  Could not parse JSON from ${path.relative(
                REPO_ROOT,
                filePath
            )}. Returning empty object.`
        )
        return {}
    }
}

function writeJson(filePath, data) {
    const dir = path.dirname(filePath)
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true })
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + "\n", "utf8")
    robotLog(`📝 Wrote ${path.relative(REPO_ROOT, filePath)}`)
}

function writeTextFile(filePath, content) {
    const dir = path.dirname(filePath)
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true })
    fs.writeFileSync(filePath, content, "utf8")
    robotLog(`📝 Wrote ${path.relative(REPO_ROOT, filePath)}`)
}

