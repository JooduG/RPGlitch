#!/usr/bin/env node

import fs from "fs";
import path from "path";
import https from "https";
import crypto from "crypto";
import dotenv from "dotenv";

// --- Configuration ---
const REPO_ROOT = process.cwd();

// --- UTILITIES ---

function copyFile(sourcePath, targetPath) {
    if (!fs.existsSync(sourcePath)) {
        console.warn(`- Skipping: Master file not found at ${path.relative(REPO_ROOT, sourcePath)}`);
        return;
    }
    const dir = path.dirname(targetPath);
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
    fs.copyFileSync(sourcePath, targetPath);
    console.log(`📝 Synced ${path.relative(REPO_ROOT, targetPath)}`);
}

function readJson(filePath) {
    if (!fs.existsSync(filePath)) {
        console.warn(`⚠️  File not found: ${path.relative(REPO_ROOT, filePath)}.`);
        return null;
    }
    try {
        let content = fs.readFileSync(filePath, 'utf8');
        if (content.charCodeAt(0) === 0xFEFF) content = content.slice(1);
        return JSON.parse(content);
    } catch (e) {
        console.error(`❌  Could not parse JSON from ${path.relative(REPO_ROOT, filePath)}.`, e);
        return null;
    }
}

function writeTextFile(filePath, content) {
    const dir = path.dirname(filePath);
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(filePath, content, 'utf8');
    console.log(`📝 Wrote ${path.relative(REPO_ROOT, filePath)}`);
}

function writeJson(filePath, data) {
    const dir = path.dirname(filePath);
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
    console.log(`📝 Wrote ${path.relative(REPO_ROOT, filePath)}`);
}


// --- SYNC LOGIC ---

function syncConfigs() {
    console.log('\n🔄 Syncing tool and IDE configuration files...');
    const configDir = path.join(REPO_ROOT, 'build', 'config');
    copyFile(path.join(configDir, '.htmlhintrc'), path.join(REPO_ROOT, '.htmlhintrc'));
    copyFile(path.join(configDir, '.markdownlint.json'), path.join(REPO_ROOT, '.markdownlint.json'));
    copyFile(path.join(configDir, 'eslint.config.mjs'), path.join(REPO_ROOT, 'eslint.config.mjs'));
    copyFile(path.join(configDir, 'stylelint.config.cjs'), path.join(REPO_ROOT, 'stylelint.config.cjs'));
    copyFile(path.join(configDir, 'jest.config.cjs'), path.join(REPO_ROOT, 'jest.config.cjs'));
    console.log('✅ Config file sync complete.');
}

function syncIgnores() {
    console.log('\n🔄 Syncing ignore files...');
    const masterIgnoresPath = path.join(REPO_ROOT, 'build', 'config', 'ignores.master.json');
    const jsconfigPath = path.join(REPO_ROOT, 'jsconfig.json');
    const settingsPath = path.join(REPO_ROOT, '.vscode', 'settings.json');
    const masterIgnores = readJson(masterIgnoresPath);
    if (!masterIgnores) {
        console.warn('⚠️  Master ignores config not found or invalid. Skipping ignore file sync.');
        return;
    }

    const header = '# Generated by build/scripts/sync.js – do not edit.\n';
    const getPatterns = (key) => masterIgnores[key] || [];

    writeTextFile(path.join(REPO_ROOT, '.gitignore'), header + getPatterns('gitignore').join('\n') + '\n');
    writeTextFile(path.join(REPO_ROOT, '.cursorignore'), header + getPatterns('ide').filter(p => !p.startsWith('/.cursor/')).join('\n') + '\n');
    writeTextFile(path.join(REPO_ROOT, '.geminiignore'), header + getPatterns('ide').filter(p => !p.startsWith('/.gemini/')).join('\n') + '\n');
    writeTextFile(path.join(REPO_ROOT, '.basicmemoryignore'), header + getPatterns('memory').filter(p => !p.startsWith('/.basicmemoryignore/')).join('\n') + '\n');
    writeTextFile(path.join(REPO_ROOT, '.stylelintignore'), header + (masterIgnores.linters?.stylelint || []).join('\n') + '\n');

    const htmlHintConfig = { ...masterIgnores.htmlhintRules };
    (masterIgnores.linters?.htmlhint || []).forEach(pattern => {
        htmlHintConfig[pattern] = false;
    });
    writeJson(path.join(REPO_ROOT, '.htmlhintrc'), htmlHintConfig);

    const jsconfig = readJson(jsconfigPath) || { compilerOptions: {}, exclude: [] };
    const allExcludes = new Set(['node_modules', ...getPatterns('gitignore'), ...getPatterns('ide')]);
    jsconfig.exclude = Array.from(allExcludes).sort();
    writeJson(jsconfigPath, jsconfig);

    try {
        const settings = readJson(settingsPath) || {};
        const newFilesExclude = masterIgnores.vscode?.filesExclude || {};
        settings['files.exclude'] = { ...(settings['files.exclude'] || {}), ...newFilesExclude };
        writeJson(settingsPath, settings);
    } catch (e) {
        console.warn(`⚠️  Could not update VS Code settings.json. Error: ${e.message}`);
    }
    console.log('✅ Ignore files sync complete.');
}

async function syncLibs() {
    console.log('\n⬇️  Fetching and verifying local libs...');
    const LOCAL_LIBS_DIR = path.join(REPO_ROOT, 'build', 'local_libs');
    const SOURCES = [
      { url: 'https://unpkg.com/@picocss/pico@2.0.6/css/pico.min.css', file: 'pico.min.css', sha256: 'dd5fd5591afd81ee21dcc117ad85c014dc3f1f19dc2d7b7d101ea0acc29274c2' },
      { url: 'https://unpkg.com/cash-dom@8.1.5/dist/cash.min.js', file: 'cash.min.js', sha256: '9a044188efdb625c5e04d1220698c099927ff16bfb434c37cd7f04dd5ee1ae1f' },
      { url: 'https://unpkg.com/dexie@4.0.7/dist/dexie.js', file: 'dexie.js', sha256: 'abf1352af0b3d46aa875be259fcee0454bfc7e95e5f8b22e071c19027b6a3b64' },
      { url: 'https://unpkg.com/dompurify@3.1.6/dist/purify.min.js', file: 'purify.min.js', sha256: 'c0845096a7c4a6741f362ac506c94c1c7d27dc603bcc1bf64a587f76f2dbe3a1' },
      { url: 'https://unpkg.com/hyperscript.org@0.9.12/dist/_hyperscript.min.js', file: '_hyperscript.min.js', sha256: 'cd737e9904a7eed1ee9639b75eb07915baad92961586d0a1fd6d998d24179de6' },
    ];

    function getFileChecksum(filePath) {
        return crypto.createHash('sha256').update(fs.readFileSync(filePath)).digest('hex');
    }

    function download(url, dest, sha256) {
        return new Promise((resolve, reject) => {
            if (fs.existsSync(dest) && getFileChecksum(dest) === sha256) return resolve();
            const tmpDest = `${dest}.tmp`;
            const file = fs.createWriteStream(tmpDest);
            https.get(url, (res) => {
                if (res.statusCode !== 200) {
                    file.close();
                    if (fs.existsSync(tmpDest)) fs.unlinkSync(tmpDest);
                    return reject(new Error(`HTTP ${res.statusCode} for ${url}`));
                }
                res.pipe(file);
                file.on('finish', () => {
                    file.close(() => {
                        const actualHash = getFileChecksum(tmpDest);
                        if (actualHash === sha256) {
                            fs.renameSync(tmpDest, dest);
                            resolve();
                        } else {
                            fs.unlinkSync(tmpDest);
                            reject(new Error(`Checksum mismatch for ${path.basename(dest)}.`));
                        }
                    });
                });
            }).on('error', (err) => {
                try { if (fs.existsSync(tmpDest)) fs.unlinkSync(tmpDest); } catch {}
                reject(err);
            });
        });
    }

    if (!fs.existsSync(LOCAL_LIBS_DIR)) fs.mkdirSync(LOCAL_LIBS_DIR, { recursive: true });

    for (const { url, file, sha256 } of SOURCES) {
        const dest = path.join(LOCAL_LIBS_DIR, file);
        try {
            await download(url, dest, sha256);
            console.log(`✅ ${file} (${(fs.statSync(dest).size / 1024).toFixed(1)} KB)`);
        } catch (err) {
            console.error(`❌ Failed to fetch ${file}: ${err.message}`);
            process.exit(1);
        }
    }
    console.log('✅ Lib sync complete.');
}

function syncMcp() {
    console.log("\n🔄 Syncing MCP configurations...");
    const masterMcpPath = path.join(REPO_ROOT, "build", "config", "mcp.master.json");
    const envPath = path.join(REPO_ROOT, ".env");
    const VSCODE_TARGET = path.join(REPO_ROOT, ".vscode", "mcp.json");
    const GENERIC_TARGETS = [path.join(REPO_ROOT, ".cursor", "mcp.json"), path.join(REPO_ROOT, ".windsurf", "mcp.json")];
    const ROOT_MCP_TARGET = path.join(REPO_ROOT, "mcp.json");

    function loadEnvFile(filePath) {
        if (!fs.existsSync(filePath)) return {};
        return dotenv.parse(fs.readFileSync(filePath));
    }

    function substituteEnvVariables(obj, envMap) {
        const replacer = (val) => String(val).replace(/\$\{([A-Za-z_][A-Za-z0-9_]*)\}/g, (match, key) => envMap[key] || match);
        const walk = (val) => {
            if (Array.isArray(val)) return val.map(walk);
            if (val && typeof val === "object") return Object.fromEntries(Object.entries(val).map(([k, v]) => [k, walk(v)]));
            return typeof val === "string" ? replacer(val) : val;
        };
        return walk(obj);
    }

    const masterMcp = readJson(masterMcpPath);
    if (!masterMcp || !masterMcp.mcpServers) {
        console.warn('⚠️  Master MCP config not found or invalid. Skipping MCP sync.');
        return;
    }

    const flattenedServers = Object.values(masterMcp.mcpServers).reduce((acc, category) => ({ ...acc, ...category }), {});
    const genericClientTemplate = { ...masterMcp, servers: flattenedServers };
    delete genericClientTemplate.mcpServers;

    const vsCodeServers = Object.fromEntries(Object.entries(flattenedServers).map(([serverName, config]) => {
        const cleanedConfig = { ...config };
        ['autoStart', 'description', 'restartOnFailure', 'httpUrl', 'autoApprove'].forEach(k => delete cleanedConfig[k]);
        if (config.httpUrl) cleanedConfig.url = config.httpUrl;
        return [serverName, cleanedConfig];
    }));
    const vsCodeClientTemplate = { ...masterMcp, servers: vsCodeServers };
    delete vsCodeClientTemplate.mcpServers;

    const envMap = { ...process.env, ...loadEnvFile(envPath) };
    const resolvedGenericClientMcp = substituteEnvVariables(genericClientTemplate, envMap);
    const resolvedVsCodeClientMcp = substituteEnvVariables(vsCodeClientTemplate, envMap);

    writeJson(VSCODE_TARGET, resolvedVsCodeClientMcp);
    GENERIC_TARGETS.forEach(target => writeJson(target, resolvedGenericClientMcp));
    writeJson(ROOT_MCP_TARGET, masterMcp);

    try {
        const geminiSettingsPath = path.join(REPO_ROOT, ".gemini", "settings.json");
        const geminiSettings = readJson(geminiSettingsPath) || {};
        geminiSettings.mcpServers = flattenedServers || {};
        writeJson(geminiSettingsPath, geminiSettings);
    } catch (error) {
        console.warn(`⚠️  Could not update Gemini settings.json. Error: ${error.message}`);
    }
    console.log("✅ MCP sync complete.");
}

function syncHub() {
    console.log("\n🔄 Generating hub file...");
    const OUTPUT_DIR = path.join(REPO_ROOT, 'build', 'output');
    const HUB_FILE = path.join(OUTPUT_DIR, 'hub.md');
    const JEST_RESULT_FILE = path.join(OUTPUT_DIR, '.jest-result.json');
    const PKG_FILE = path.join(REPO_ROOT, 'package.json');

    function getRepoTree() {
        const exclude = [/node_modules/, /\.git/, /\.codacy/, /\.gemini/, /\.github/, /\.vscode/, /build\/local_libs/, /^\.yarn/, /^\.idea/];
        const output = [];
        const hubFileDir = path.join(REPO_ROOT, 'build', 'output');

        function walk(dir, depth) {
            if (depth > 2) return;
            let entries;
            try {
                entries = fs.readdirSync(dir, { withFileTypes: true })
                    .filter(entry => !exclude.some(rx => rx.test(entry.name)))
                    .sort((a, b) => {
                        if (a.isDirectory() && !b.isDirectory()) return -1;
                        if (!a.isDirectory() && b.isDirectory()) return 1;
                        return a.name.localeCompare(b.name);
                    });
            } catch { return; }

            entries.forEach(entry => {
                const prefix = '  '.repeat(depth) + '- ';
                if (entry.isDirectory()) {
                    output.push(prefix + `**${entry.name}/**`);
                    walk(path.join(dir, entry.name), depth + 1);
                } else if (entry.name.endsWith('.md')) {
                    const fullPath = path.join(dir, entry.name);
                    const linkPath = path.relative(hubFileDir, fullPath).replace(/\\/g, '/');
                    output.push(prefix + `[${entry.name}](${linkPath})`);
                }
            });
        }
        output.push('## Repository Map');
        walk(REPO_ROOT, 0);
        return output.join('\n');
    }

    function getTestStats() {
        try {
            if (fs.existsSync(JEST_RESULT_FILE)) {
                const results = JSON.parse(fs.readFileSync(JEST_RESULT_FILE, 'utf8'));
                const { numTotalTests, numPassedTests } = results;
                const status = numTotalTests === numPassedTests ? '✅' : '❌';
                return `${status} ${numPassedTests}/${numTotalTests} passed`;
            }
        } catch { /* ignore */ }
        return 'No test results found.';
    }

    function getPackageScripts() {
        try {
            const pkg = JSON.parse(fs.readFileSync(PKG_FILE, 'utf8'));
            const scripts = pkg.scripts || {};
            return Object.entries(scripts).map(([key, value]) => `  - \`npm run ${key}\`: ${value}`).join('\n');
        } catch {
            return 'Could not load scripts.';
        }
    }

    const content = `
# 🧭 Hub: Repository Overview
- **Tests:** ${getTestStats()}
- **Last updated:** ${new Date().toUTCString()}
---
## Repository Map
${getRepoTree()}
---
## Common Commands
${getPackageScripts()}
`;
    if (!fs.existsSync(OUTPUT_DIR)) fs.mkdirSync(OUTPUT_DIR, { recursive: true });
    fs.writeFileSync(HUB_FILE, content.trim() + '\n', 'utf8');
    console.log(`✅ Hub file generated at: ${path.relative(REPO_ROOT, HUB_FILE)}`);
}

// --- MAIN EXECUTION ---

async function main() {
    const args = process.argv.slice(2);
    const runAll = args.length === 0 || args.includes('--all');

    if (runAll || args.includes('--configs')) syncConfigs();
    if (runAll || args.includes('--ignores')) syncIgnores();
    if (runAll || args.includes('--libs')) await syncLibs();
    if (runAll || args.includes('--mcp')) syncMcp();
    if (runAll || args.includes('--hub')) syncHub();

    console.log('\n✨ Sync process finished.');
}

main();
