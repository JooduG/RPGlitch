#!/usr/bin/env node
/* eslint-disable no-console */
/// Single Source of Truth ignore sync
/// - Reads: build/config/ignores.master.json
/// - Writes: .gitignore, .cursorignore (repo root)
/// - Updates: .vscode/settings.json (markdownlint.ignore + files.exclude)
///
/// No other *.ignore files will be written. VS Code & CLI are expected to
/// consume ignores via these three endpoints only.

const fs = require('fs');
const fsp = require('fs/promises');
const path = require('path');

const ROOT = process.cwd();
const CONFIG_DIR = path.join(ROOT, 'build', 'config');
const MASTER = path.join(CONFIG_DIR, 'ignores.master.json');
const VSCODE_DIR = path.join(ROOT, '.vscode');
const SETTINGS = path.join(VSCODE_DIR, 'settings.json');

const GITIGNORE = path.join(ROOT, '.gitignore');
const CURSORIGNORE = path.join(ROOT, '.cursorignore');

function ensureDirSync(dir) {
  if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
}

async function readJson(file, fallback = {}) {
  try {
    const raw = await fsp.readFile(file, 'utf8');
    return JSON.parse(raw);
  } catch {
    return fallback;
  }
}

async function writeText(file, content) {
  ensureDirSync(path.dirname(file));
  await fsp.writeFile(file, content, 'utf8');
}

function banner(title) {
  return [
    `# ─────────────────────────────────────────────────────────`,
    `# ${title}`,
    `# This file is GENERATED by build/scripts/sync-ignores.js`,
    `# Edit: build/config/ignores.master.json (single source of truth)`,
    `# ─────────────────────────────────────────────────────────`,
    ``,
  ].join('\n');
}

function normalizeList(x) {
  if (!x) return [];
  if (Array.isArray(x)) return x.map(String);
  // allow { "pattern": true/false } objects to degrade to keys with truthy values
  if (typeof x === 'object') {
    return Object.entries(x)
      .filter(([, v]) => !!v)
      .map(([k]) => String(k));
  }
  return [];
}

function mergeBoolRecord(base = {}, add = {}) {
  const out = { ...base };
  for (const [k, v] of Object.entries(add)) out[k] = !!v;
  return out;
}

(async () => {
  const master = await readJson(MASTER);
  if (!Object.keys(master).length) {
    console.error(`❌ Missing or invalid ${path.relative(ROOT, MASTER)}. Create a JSON file like:

{
  "gitignore": [
    "node_modules/",
    "build/output/",
    "build/local_libs/",
    "local_libs/",
    ".cache/",
    "dist/"
  ],
  "cursorignore": [
    "node_modules/",
    "build/output/",
    "build/local_libs/",
    "local_libs/"
  ],
  "markdownlint": [
    "memory-bank/**",
    "build/output/**",
    "node_modules/**"
  ],
  "vscodeFilesExclude": {
    "build/output/**": true,
    "build/local_libs/**": true,
    "local_libs/**": true
  }
}
`);
    process.exit(1);
  }

  // 1) .gitignore
  const gitLines = normalizeList(master.gitignore);
  const gitContent =
    banner('AUTO-GENERATED .gitignore') +
    (gitLines.length ? gitLines.join('\n') + '\n' : '');
  await writeText(GITIGNORE, gitContent);

  // 2) .cursorignore
  const cursorLines = normalizeList(master.cursorignore);
  const cursorContent =
    banner('AUTO-GENERATED .cursorignore') +
    (cursorLines.length ? cursorLines.join('\n') + '\n' : '');
  await writeText(CURSORIGNORE, cursorContent);

  // 3) .vscode/settings.json
  ensureDirSync(VSCODE_DIR);
  const settings = await readJson(SETTINGS, {});
  // markdownlint ignores
  const mdIgnores = normalizeList(master.markdownlint);
  if (mdIgnores.length) {
    settings['markdownlint.ignore'] = mdIgnores;
  } else {
    // keep user setting if master omitted it
    settings['markdownlint.ignore'] = settings['markdownlint.ignore'] || [];
  }

  // files.exclude (for Explorer visibility)
  const fileEx = mergeBoolRecord(
    settings['files.exclude'] || {},
    master.vscodeFilesExclude || {}
  );
  if (Object.keys(fileEx).length) settings['files.exclude'] = fileEx;

  await writeText(SETTINGS, JSON.stringify(settings, null, 2) + '\n');

  // Summary
  console.log(`🧠 Source of truth: ${path.relative(ROOT, MASTER)}`);
  console.log(`📝 wrote ${path.relative(ROOT, GITIGNORE)} (${gitLines.length} patterns)`);
  console.log(`📝 wrote ${path.relative(ROOT, CURSORIGNORE)} (${cursorLines.length} patterns)`);
  console.log(`🛠  updated ${path.relative(ROOT, SETTINGS)} (markdownlint.ignore + files.exclude)`);
  console.log(`✅ Ignore settings synchronized`);
})().catch((err) => {
  console.error('❌ sync-ignores failed:', err);
  process.exit(1);
});
