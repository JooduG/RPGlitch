#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

// --- PATHS ---
const REPO_ROOT = path.resolve(__dirname, '..', '..');
const masterIgnoresPath = path.join(REPO_ROOT, 'build', 'config', 'ignores.master.json');
const jsconfigPath = path.join(REPO_ROOT, 'jsconfig.json');

// --- UTILITIES ---
function readJson(filePath) {
    if (!fs.existsSync(filePath)) return null;
    try {
        let content = fs.readFileSync(filePath, 'utf8');
        if (content.charCodeAt(0) === 0xFEFF) content = content.slice(1);
        const jsonString = content.replace(/\/\*[\s\S]*?\*\/|\/\/.*/g, '');
        return JSON.parse(jsonString);
    } catch (err) {
        console.warn(`⚠️  Could not read/parse ${path.relative(REPO_ROOT, filePath)}.`);
        return null;
    }
}

function writeTextFile(filePath, content) {
    const dir = path.dirname(filePath);
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(filePath, content, 'utf8');
    console.log(`📝 Wrote ${path.relative(REPO_ROOT, filePath)}`);
}

function writeJson(filePath, data) {
    const dir = path.dirname(filePath);
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
    console.log(`📝 Wrote ${path.relative(REPO_ROOT, filePath)}`);
}

// --- MAIN LOGIC ---
function syncIgnoreFiles() {
    console.log('Syncing ignore files...');
    const masterIgnores = readJson(masterIgnoresPath);
    if (!masterIgnores) {
        console.warn('⚠️  Master ignores config not found. Skipping ignore file sync.');
        return;
    }

    const header = '# Generated by build/scripts/sync-ignores.js – do not edit.\n';
    
    // This function now safely gets patterns, defaulting to an empty array.
    const getPatterns = (key) => masterIgnores[key]?.patterns || [];

    // --- Generate ignore files ---
    writeTextFile(path.join(REPO_ROOT, '.gitignore'), header + getPatterns('gitignore').join('\n') + '\n');
    writeTextFile(path.join(REPO_ROOT, '.cursorignore'), header + getPatterns('ide').join('\n') + '\n');
    writeTextFile(path.join(REPO_ROOT, '.geminiignore'), header + getPatterns('ide').join('\n') + '\n');
    writeTextFile(path.join(REPO_ROOT, '.basicmemoryignore'), header + getPatterns('memory').join('\n') + '\n');

    // --- Generate linter ignores ---
    writeJson(path.join(REPO_ROOT, 'build', 'config', 'ignore.eslint.json'), { ignorePatterns: masterIgnores.linters?.eslint || [] });
    writeTextFile(path.join(REPO_ROOT, 'build', 'config', '.stylelintignore'), header + (masterIgnores.linters?.stylelint || []).join('\n') + '\n');
    writeTextFile(path.join(REPO_ROOT, 'build', 'config', '.htmlhintignore'), header + (masterIgnores.linters?.htmlhint || []).join('\n') + '\n');
    writeTextFile(path.join(REPO_ROOT, 'build', 'config', '.markdownlintignore'), header + (masterIgnores.linters?.markdownlint || []).join('\n') + '\n');

    // --- Update jsconfig.json ---
    const jsconfig = readJson(jsconfigPath) || { compilerOptions: {}, exclude: [] };
    const allExcludes = new Set([
        'node_modules', 
        ...(getPatterns('gitignore')), 
        ...(getPatterns('ide'))
    ]);
    jsconfig.exclude = Array.from(allExcludes).sort();
    writeJson(jsconfigPath, jsconfig);
    
    console.log('Ignore files sync process complete.');
}

syncIgnoreFiles();
